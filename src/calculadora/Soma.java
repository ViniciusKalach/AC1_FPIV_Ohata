package calculadora;

/**
 * Esta classe fornece um método para realizar a operação de adição(soma).
 *
 * @author Vinícius Kalach
 * @since 1.0
 * @version 1.0
 */
public class Soma {
  
	/**
     * Soma dois números inteiros.
     *
     * @param a O primeiro número inteiro a ser somado.
     * @param b O segundo número inteiro a ser somado.
     * @return O resultado da soma de a e b.
     */	
    public int somar(int a, int b){
        return a + b;
    }
}
