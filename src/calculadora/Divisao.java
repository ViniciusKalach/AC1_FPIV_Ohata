package calculadora;

/**
 * Esta classe fornece um método para realizar a operação de divisão.
 *
 * @author Vinícius Kalach
 * @since 1.0
 * @version 1.0
 */
public class Divisao {
    
	/**
     * Divide dois números inteiros.
     *
     * @param a O dividendo (número inteiro a ser dividido).
     * @param b O divisor (número inteiro pelo qual a divisão será realizada).
     * @return O resultado da divisão de a por b.
     */	
    public int dividir(int a, int b){
        return a / b;
    }
}
