package calculadora;

/**
 * Esta classe fornece um método para realizar a operação de multiplicação.
 *
 * @author Vinícius Kalach
 * @since 1.0
 * @version 1.0
 */
public class Multiplicacao {
    
	/**
     * Multiplica dois números inteiros.
     *
     * @param a O primeiro número inteiro a ser multiplicado.
     * @param b O segundo número inteiro a ser multiplicado.
     * @return O resultado da multiplicação  de a por b.
     */	
    public int multiplicar(int a, int b){
        return a * b;
    }
}
