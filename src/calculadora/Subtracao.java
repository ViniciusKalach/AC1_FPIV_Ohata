package calculadora;

/**
 * Esta classe fornece um método para realizar a operação de subtração.
 *
 * @author Vinícius Kalach
 * @since 1.0
 * @version 1.0
 */
public class Subtracao {
    
	/**
     * Subtrai dois números inteiros.
     *
     * @param a O número inteiro a ser subtraído.
     * @param b O número inteiro que será subtraído de a.
     * @return O resultado da subtração de a e b.
     */	
    public int subtrair(int a, int b){
        return a - b;
    }
}
